public class OpDemo5 {
	public static void main(String[] args) {
		// 증감 연산자 : ++, --

		int a = 10;

		a++;			// a의 값을 1 증가시킨다.
		System.out.println(a);		// 11

		++a;			// a의 값을 1 증가시킨다.
		System.out.println(a);		// 12

		int b = 100;

		b--;			// b의 값을 1 감소시킨다.
		System.out.println(b);		// 99

		--b;			// b의 값을 1 감소시킨다.
		System.out.println(b);		// 98

		
	}
}
