public class OpDemo1 {
	public static void main(String[] args) {
		// 산술연산자 : + - * / %(나머지연산자, modular 연산자, 암호화분야)
		int num1 = 7;
		int num2 = 3;

		System.out.println(num1/num2);	// 몫을 계산한다. ---> 2
		System.out.println(num1%num2);	// 나머지를 계산한다. ---> 1
										// 특징: 나머지는 나누는 수보다는 작다.

	}
}
