public class ForDemo1 {
	public static void main (String[] args) {
		
		// i는 1인데 i가 5보다 작을때까지 1씩 증가해서 출력하라
		for (int i=1; i<=5; i++) {			// 초기식, 조건식, 증감식
			System.out.println(i + "번째 실행");	
			
			// 증감식이 없으면 '무한루프' 함
			// i 라는 값은 처음시작하는 포지션 설정하는데 중요함

			// 1번째 실행
			// 2번째 실행
			// 3번째 실행
			// 4번째 실행
			// 5번째 실행
			
		}
	}
}
