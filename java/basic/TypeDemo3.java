public class TypeDemo3 {
	public static void main(String[] args) {
		// 실수 타입 : float, double

		byte b = 10;

		double num1 = 3.14;
		// 모든 실수는 double 범위에서 판단된다.
		// 실수 끝에 f를 붙이면 float 범위에서 판단하게 한다.
		float num2 = 3.14f;

		System.out.println(num1);
		System.out.println(num2);
	
	}
}