package kr.co.jhta.vo;

public class Student {
	private int no;
	private String name;

	public Student() {
	
	}

	public int getNo() {
		return no;
	}
	public String getName() {
		return name;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public void setName(String name) {
		this.name = name;
	}
}